# Lecția precedentă am vorbit despre variabilele integer, acum e rândul la float
# Float (prescurtare pentru număr cu virgulă mobilă) este un număr, pozitiv sau negativ, care conține una sau mai multe zecimale
# De exemplu: 1.0, -100.5, 100.0
# Să creăm o variabilă numită z și să-i atribuim valoarea 1.0
z = 1.0
# Acum să afișăm tipul lui z
print(type(z))
# Ar trebui să obțineți <class 'float'>
# La fel cum puteți face operații cu numere integer, puteți face același lucru în Python cu numere float
# De exemplu, să creăm o variabilă numită a și să-i atribuim valoarea 5.0
a = 5.0
# Acum să creăm o variabilă numită b și să-i atribuim valoarea 2.0
b = 2.0
# Acum să afișăm suma lui a și b
print(a + b)
# Ar trebui să obțineți 7.0
# Acum să afișăm diferența dintre a și b
print(a - b)
# Ar trebui să obțineți 3.0
# Acum să afișăm produsul lui a și b
print(a * b)
# Ar trebui să obțineți 10.0
# Acum să afișăm împărțirea lui a și b
print(a / b)
# Ar trebui să obțineți 2.5
# Acum să afișăm restul împărțirii lui a și b
print(a % b)
# Ar trebui să obțineți 1.0
# Acum să afișăm rezultatul lui a ridicat la puterea lui b
print(a ** b)
# Ar trebui să obțineți 25.0
# Acum să vorbim despre împărțirea întreagă a lui a și b
print(a // b)
# Ar trebui să obțineți 2.0

# Odată ce unul dintre elementele unei operații este float, rezultatul va fi de asemenea float
# De exemplu, să creăm o variabilă numită c și să-i atribuim valoarea 5
c = 5
# Acum să creăm o variabilă numită d și să-i atribuim valoarea 2.0
d = 2.0
# Acum să afișăm suma lui c și d
print(c + d)
# Ar trebui să obțineți 7.0

